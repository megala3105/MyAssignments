package week1.day2;

public class LearnSwitchCase {

	public static void main(String[] args) {
		//switch case
		int num=-1;
		switch(num)
		{
		case 0:
			System.out.println("The number is zero");
			break;
		case 1:
			System.out.println("The number is 1");
			break;
			default:
				System.out.println("The number is negative");
				
	}

}
}