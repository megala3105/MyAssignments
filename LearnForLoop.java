package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {
		// Print 1 to 5
		
		System.out.println("Print 1 to 5");
		for(int i=1;i<=5;i++)
		{
			
			System.out.println(i);
			}

		//Print number from 5 to 1
		System.out.println("Print from 5 to 1");
		for(int i=5;i>=1;i--)
		{
			System.out.println(i);
		}
	}

}
